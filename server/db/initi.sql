
CREATE TABLE "User" (
    id uuid NOT NULL PRIMARY KEY,
    email text UNIQUE NOT NULL,
    password text NOT NULL,
    "firstName" text NOT NULL,
    "lastName" text NOT NULL,
    avatar text,
    role text CHECK( role IN ('USER', 'ADMIN', 'VENDOR')) DEFAULT 'USER' NOT NULL,
    bio text,
    "createdAt" timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    "updatedAt" timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    address text,
    borough text,
    "businessName" text,
    city text,
    "companyWebsiteUrl" text,
    "districtBoroNumber" text,
    "famisNumber" text,
    "gradeBand" text,
    "hasMTAC" boolean DEFAULT false NOT NULL,
    "inquiryContactName" text,
    "inquiryContactPhone" text,
    "languagesSpoken" text,
    "nycMwbeNumber" text,
    "nysMwbeNumber" text,
    "officeDepartment" text,
    "phoneNumber" text,
    "pitchVideoUrl" text,
    "registrationType" text,
    "servicesOffered" text,
    state text,
    title text,
    "businessLogoUrl" text
);

